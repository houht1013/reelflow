import { strToU8, zipSync } from 'fflate';

export type DraftPackageJob = {
  id: string;
  workspaceId: string;
  templateId: string;
  templateCode?: string;
  templateName?: string;
  inputParams: Record<string, unknown>;
  estimatedCredits: string;
  actualCredits: string;
  renderMp4Requested: boolean;
  createdAt?: Date | string;
  completedAt?: Date | string | null;
};

export type DraftPackageStage = {
  stageCode: string;
  status: string;
  outputSnapshot?: unknown;
};

export type DraftPackageAsset = {
  assetType: string;
  storageProvider?: string | null;
  storageKey?: string | null;
  url?: string | null;
  mimeType?: string | null;
  status?: string;
  metadata?: unknown;
};

export type DraftPackageUsageRecord = {
  resourceType: string;
  provider: string;
  model?: string | null;
  usageAmount: string;
  usageUnit: string;
  creditCost: string;
};

export type DraftPackageInput = {
  job: DraftPackageJob;
  stages: DraftPackageStage[];
  assets: DraftPackageAsset[];
  usageRecords: DraftPackageUsageRecord[];
};

const encoder = new TextEncoder();

export function getDraftPackageFileName(jobId: string): string {
  return `reelflow-draft-${jobId}.zip`;
}

export function getDraftPackageStorageKey(jobId: string): string {
  return `reelflow/jobs/${jobId}/draft-package.zip`;
}

export function buildDraftPackageBuffer(input: DraftPackageInput): Buffer {
  const packageFiles = buildDraftPackageFiles(input);
  const zipped = zipSync(packageFiles, { level: 6 });
  return Buffer.from(zipped);
}

export function buildDraftPackageManifest(input: DraftPackageInput) {
  return {
    schemaVersion: 'reelflow.draft-package.v1',
    generatedAt: new Date().toISOString(),
    job: {
      id: input.job.id,
      workspaceId: input.job.workspaceId,
      templateId: input.job.templateId,
      templateCode: input.job.templateCode,
      templateName: input.job.templateName,
      estimatedCredits: input.job.estimatedCredits,
      actualCredits: input.job.actualCredits,
      renderMp4Requested: input.job.renderMp4Requested,
      createdAt: serializeDate(input.job.createdAt),
      completedAt: serializeDate(input.job.completedAt),
    },
    videoSpec: {
      width: 1080,
      height: 1920,
      fps: 30,
      format: 'jianying-draft',
    },
    files: [
      'manifest.json',
      'project.workflow.json',
      'capcut_mate_payload.json',
      'assets/asset-index.json',
      'scripts/run-capcut-mate.ps1',
      'scripts/capcut-mate-requests.http',
      'docs/capcut-mate-local-setup.md',
      'README.md',
    ],
  };
}

export function buildDraftPackageMetadata(jobId: string) {
  return {
    mode: 'local_draft',
    downloadable: true,
    fileName: getDraftPackageFileName(jobId),
    storageKey: getDraftPackageStorageKey(jobId),
    packageSchemaVersion: 'reelflow.draft-package.v1',
    adapter: 'capcut-mate',
    note: 'Generated by Reelflow local draft adapter. The package includes capcut-mate payload files for conversion.',
  };
}

function buildDraftPackageFiles(input: DraftPackageInput): Record<string, Uint8Array> {
  const manifest = buildDraftPackageManifest(input);
  const workflow = buildWorkflowProject(input);
  const payload = buildCapcutMatePayload(input, workflow);
  const assetIndex = input.assets.map((asset) => ({
    assetType: asset.assetType,
    storageProvider: asset.storageProvider,
    storageKey: asset.storageKey,
    url: asset.url,
    mimeType: asset.mimeType,
    status: asset.status,
    metadata: asset.metadata,
  }));

  return {
    'manifest.json': jsonFile(manifest),
    'project.workflow.json': jsonFile(workflow),
    'capcut_mate_payload.json': jsonFile(payload),
    'assets/asset-index.json': jsonFile(assetIndex),
    'scripts/run-capcut-mate.ps1': strToU8(buildCapcutMatePowerShellScript()),
    'scripts/capcut-mate-requests.http': strToU8(buildCapcutMateHttpFile(payload)),
    'docs/capcut-mate-local-setup.md': strToU8(buildCapcutMateSetupDoc()),
    'README.md': strToU8(buildReadme(input)),
  };
}

function buildWorkflowProject(input: DraftPackageInput) {
  const topic = stringParam(input.job.inputParams.topic) || stringParam(input.job.inputParams.claim) || 'Untitled topic';
  const tone = stringParam(input.job.inputParams.tone) || 'clear';
  const audience = stringParam(input.job.inputParams.audience) || 'general audience';
  const examples = stringParam(input.job.inputParams.examples);
  const durationSeconds = 45;

  const scenes = [
    {
      id: 'hook',
      title: 'Opening hook',
      startMs: 0,
      endMs: 8000,
      narration: `Here is a ${tone} opening for ${audience}: ${topic}.`,
      visualPrompt: `Simple vertical short-video opening scene about ${topic}.`,
    },
    {
      id: 'body',
      title: 'Main explanation',
      startMs: 8000,
      endMs: 35000,
      narration: examples ? `Use this example as supporting material: ${examples}` : `Explain the idea in plain language: ${topic}.`,
      visualPrompt: `Structured explainer visuals, clean composition, mobile-first frame.`,
    },
    {
      id: 'close',
      title: 'Closing note',
      startMs: 35000,
      endMs: durationSeconds * 1000,
      narration: `Close with one actionable takeaway for ${audience}.`,
      visualPrompt: `Concise closing frame with strong contrast and readable composition.`,
    },
  ];

  return {
    schemaVersion: 'reelflow.workflow-project.v1',
    canvas: {
      width: 1080,
      height: 1920,
      fps: 30,
    },
    topic,
    tone,
    audience,
    durationMs: durationSeconds * 1000,
    scenes,
    stages: input.stages.map((stage) => ({
      code: stage.stageCode,
      status: stage.status,
      outputSnapshot: stage.outputSnapshot,
    })),
    usage: input.usageRecords,
  };
}

export function buildCapcutMatePayload(input: DraftPackageInput, workflow: ReturnType<typeof buildWorkflowProject>) {
  const captions = workflow.scenes.map((scene) => ({
    start: scene.startMs * 1000,
    end: scene.endMs * 1000,
    text: scene.narration,
    font_size: 13,
  }));

  return {
    adapter: 'capcut-mate',
    adapterVersion: 'capcut-mate.openapi.v1',
    defaultBaseUrl: 'http://localhost:30000',
    baseUrlEnv: 'CAPCUT_MATE_BASE_URL',
    source: {
      project: 'Reelflow',
      jobId: input.job.id,
      templateCode: input.job.templateCode,
      templateName: input.job.templateName,
    },
    requirements: [
      'Start a capcut-mate service before running the request script.',
      'The service exposes /openapi/capcut-mate/v1/create_draft, /add_captions, and /save_draft.',
      'The current MVP package writes captions and the normalized workflow project. Real image/audio files can be added after provider assets are connected.',
    ],
    steps: [
      {
        id: 'create_draft',
        description: 'Create a vertical 1080x1920 Jianying draft.',
        method: 'POST',
        path: '/openapi/capcut-mate/v1/create_draft',
        captures: {
          draftUrl: 'draft_url',
        },
        body: {
          width: workflow.canvas.width,
          height: workflow.canvas.height,
        },
      },
      {
        id: 'add_captions',
        description: 'Add generated narration as timeline captions.',
        method: 'POST',
        path: '/openapi/capcut-mate/v1/add_captions',
        dependsOn: 'create_draft',
        body: {
          draft_url: '{{draft_url}}',
          captions: JSON.stringify(captions),
          text_color: '#ffffff',
          border_color: '#111111',
          alignment: 1,
          font_size: 13,
          transform_y: 720,
        },
      },
      {
        id: 'save_draft',
        description: 'Persist the generated draft after all editing operations.',
        method: 'POST',
        path: '/openapi/capcut-mate/v1/save_draft',
        dependsOn: 'add_captions',
        body: {
          draft_url: '{{draft_url}}',
        },
      },
    ],
    notes: [
      'The first step returns draft_url. Substitute it into later steps.',
      'Image and audio provider outputs can be appended as add_images and add_audios steps when real assets are available.',
      'For local Windows verification, run scripts/run-capcut-mate.ps1 after starting capcut-mate.',
    ],
  };
}

