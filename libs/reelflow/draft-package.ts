function buildCapcutMatePowerShellScript(): string {
  return [
    'param(',
    '  [string]$BaseUrl = $env:CAPCUT_MATE_BASE_URL',
    ')',
    '',
    'if ([string]::IsNullOrWhiteSpace($BaseUrl)) {',
    '  $BaseUrl = "http://localhost:30000"',
    '}',
    '',
    '$PayloadPath = Join-Path $PSScriptRoot "..\\capcut_mate_payload.json"',
    'if (!(Test-Path -LiteralPath $PayloadPath)) {',
    '  throw "capcut_mate_payload.json was not found next to the scripts folder."',
    '}',
    '',
    '$Payload = Get-Content -LiteralPath $PayloadPath -Raw | ConvertFrom-Json',
    '$DraftUrl = $null',
    '',
    'foreach ($Step in $Payload.steps) {',
    '  $Body = $Step.body | ConvertTo-Json -Depth 20 -Compress',
    '  if ($DraftUrl) {',
    '    $Body = $Body.Replace("{{draft_url}}", $DraftUrl)',
    '  }',
    '',
    '  $Uri = $BaseUrl.TrimEnd("/") + $Step.path',
    '  Write-Host ("Running " + $Step.id + " -> " + $Uri)',
    '  $Response = Invoke-RestMethod -Method $Step.method -Uri $Uri -ContentType "application/json" -Body $Body',
    '',
    '  if ($Response.draft_url) {',
    '    $DraftUrl = [string]$Response.draft_url',
    '  }',
    '}',
    '',
    'if (!$DraftUrl) {',
    '  throw "capcut-mate did not return a draft_url."',
    '}',
    '',
    'Write-Host ""',
    'Write-Host "Draft URL:"',
    'Write-Host $DraftUrl',
    '',
  ].join('\n');
}

function buildCapcutMateHttpFile(payload: ReturnType<typeof buildCapcutMatePayload>): string {
  const createDraft = payload.steps.find((step) => step.id === 'create_draft')?.body;
  const addCaptions = payload.steps.find((step) => step.id === 'add_captions')?.body;
  const saveDraft = payload.steps.find((step) => step.id === 'save_draft')?.body;

  return [
    '@baseUrl = http://localhost:30000',
    '@draftUrl = replace_with_create_draft_response_draft_url',
    '',
    '### Create draft',
    'POST {{baseUrl}}/openapi/capcut-mate/v1/create_draft',
    'Content-Type: application/json',
    '',
    JSON.stringify(createDraft, null, 2),
    '',
    '### Add captions',
    'POST {{baseUrl}}/openapi/capcut-mate/v1/add_captions',
    'Content-Type: application/json',
    '',
    JSON.stringify(addCaptions, null, 2),
    '',
    '### Save draft',
    'POST {{baseUrl}}/openapi/capcut-mate/v1/save_draft',
    'Content-Type: application/json',
    '',
    JSON.stringify(saveDraft, null, 2),
    '',
  ].join('\n');
}

